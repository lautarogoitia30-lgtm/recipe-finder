/**
 * Traduce un ingrediente al español
 */
export function translateIngredient(ingredient) {
  if (!ingredient) return ingredient
  const lower = ingredient.toLowerCase().trim()
  return INGREDIENTS_ES[lower] || ingredient
}

/**
 * Traduce una categoría al español
 */
export function translateCategory(category) {
  if (!category) return category
  return CATEGORIES_ES[category] || category
}

/**
 * Traduce una cocina/región al español
 */
export function translateArea(area) {
  if (!area) return area
  return AREAS_ES[area] || area
}

/**
 * Traduce una medida al español
 */
export function translateMeasure(measure) {
  if (!measure) return measure
  let result = measure
  // Replace known English measures
  for (const [en, es] of Object.entries(MEASURES_ES)) {
    const regex = new RegExp(`\\b${en}\\b`, 'gi')
    result = result.replace(regex, es)
  }
  return result
}
